public class times_method {
    public static void main(String[] args) {
        for(int i=1;i<=9;i++){
            for(int j=1;j<=9;j++){
                if(i>=j){
                System.out.print(j+"*"+i+"="+j*i+"       ");
            }}
            System.out.println(" ");
        }
    }

}
